#include<stdio.h>
int main()
{
	int x,y;
	printf("Write 2 numbers!\n");
	scanf("%d %d",&x,&y);
	printf("x = %d\n", x);
	printf("y = %d\n\n", y);
	printf("x>y : %d\n",x>y);
	printf("x>=y : %d\n",x>=y);
	printf("x<y : %d\n",x<y);
	printf("x<=y : %d\n",x<=y);
	printf("x==y : %d\n",x==y);
	printf("x!=y : %d\n",x!=y);
}
